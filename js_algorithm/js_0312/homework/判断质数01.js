// 判断45887 是不是质数

// 令i 为45887的约数，从1遍历到45887，
// 令sum += i, 若，sum+1 = 45887+1 ,则次数为质数

var sum = 0
for(i = 1; i<= 45887;i++){
    
    if (45887 % i == 0) {
        // 用累加器把i的所有因数全部累加起来
        // console.log(i);
        sum += i;
    };
};
if (sum == 45888){
    console.log('45887 为质数');
}else{
    console.log('45887 不为质数');
};