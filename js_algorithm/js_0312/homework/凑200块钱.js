
// 令 i,j,k 为1，2，5 前面的系数，进行遍历
// 当 i = 1, 当 j = 1, 遍历一遍k, 判断，若符合， 则记录，i,j,k
// 当 i = 1, 当 j = 2, 遍历一遍k, 判断，若符合， 则记录，i,j,k
// 故 当 1 = 1 时，遍历一遍 j ,内嵌套，k的for循环
// 最外圈则循环 i



var sum = 0;
for (var i = 0; i <= 200; i++) {
    for (var j = 0; j <= 100; j++) {
        for (var k = 0; k <= 20; k++) {
            sum = 1 * i + 2 * j + 5 * k;
            if (sum == 200) {
                console.log(i, j, k);
            };
        };
    };
};
